package day19_WhileLoopDoWhileLoopScope;

public class Q04DoWhileLoop {

    public static void main(String[] args) {

        // Do while loop
        int i = 10;

        do {
            System.out.println(i);
            i++;
        }while (i<5);


        System.out.println("==========================");

        // while loop

        int j = 10;
        while (j<5){

            System.out.println(j);
            i++;
        }


    }
}
