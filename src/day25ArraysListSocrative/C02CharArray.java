package day25ArraysListSocrative;

public class C02CharArray {

    public static void main(String[] args) {
        char arr[] = new char[3];

        arr[0] = 'A';

        arr[1] = 'L';

        arr[2] = 'I';



        for(int i=2; i>0; i--) {

            System.out.print(arr[i]);

        }
    }
}
