package SocrativeQuestionForLoop;

public class Q05 {

    public static void main(String[] args) {

        for(int i=1; i<10; i=i+1) {

            if(i%3==0) {

                System.out.print(i + " ");

            }

        }


        for(int i=1; i<=9; i=i+3) {

            if(i%3==0) {

                System.out.print(i + " ");

            }

        }
    }
}
